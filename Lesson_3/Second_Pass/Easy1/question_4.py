'''
Using the following string, print a string that contains the same value,
but using all lowercase letters except for the first character, which
should be capitalized.
'''
munsters_description = "the Munsters are CREEPY and Spooky."
# => 'The munsters are creepy and spooky.'
'''
string objects have built in methods. The built in method we would use here is the capitalize() method.
'''

print(munsters_description.capitalize())